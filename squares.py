# Task 1 - Squares

# Start a counter at 1
num = 1

# Loop until the square of the number exceeds 2000
while num ** 2 <= 2000:
    # Calculate the square
    square = num ** 2
    # Display the number and its square
    print(f"{num} squared is {square}")
    # Increment the counter
    num += 1
